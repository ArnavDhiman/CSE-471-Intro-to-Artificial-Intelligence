class ParticleFilter(InferenceModule):
    """
    A particle filter for approximately tracking a single ghost.
    """
    def __init__(self, ghostAgent, numParticles=300):
        InferenceModule.__init__(self, ghostAgent)
        self.setNumParticles(numParticles)

    def setNumParticles(self, numParticles):
        self.numParticles = numParticles

    def initializeUniformly(self, gameState):
        """
        Initialize a list of particles. Use self.numParticles for the number of
        particles. Use self.legalPositions for the legal board positions where
        a particle could be located. Particles should be evenly (not randomly)
        distributed across positions in order to ensure a uniform prior. Use
        self.particles for the list of particles.
        """
        self.particles = []
        i = 0
        while i < self.numParticles:
            for position in self.legalPositions:
                self.particles.append(position)
                i+=1

    def observeUpdate(self, observation, gameState):
        """
        Update beliefs based on the action observation and the game state.

        The observation is the noisy action the ghost you are tracking has taken.
        Use this to perform an update of the position of the ghosts based on the observations. 

        There is one special case that a correct implementation must handle.
        When all particles receive zero weight, the list of particles should
        be reinitialized by calling initializeUniformly. The total method of
        the DiscreteDistribution may be useful.
        # """        
        
        distribution = DiscreteDistribution()
        pacman = gameState.getPacmanPosition()
        jail = self.getJailPosition()
        belief = self.getBeliefDistribution()
        #print(observation)
        
        for p in self.particles:
            newghostpos = list(p)
            if observation == Directions.NORTH:
                newghostpos[0] -= 1

            if observation == Directions.SOUTH:
                newghostpos[0] += 1

            if observation == Directions.EAST:
                newghostpos[1] -= 1

            if observation == Directions.WEST:
                newghostpos[1] += 1
            
            distribution[p] += self.getObservationProb(noisyAction = observation, oldGhostPos = p, newGhostPos = newghostpos, jailPosition =jail)

        if not any(distribution.values()):
            self.initializeUniformly(gameState)
        else:
            self.particles = [ distribution.sample() for x in range(self.numParticles) ]



    def getBeliefDistribution(self):
        """
        Return the agent's current belief state, a distribution over ghost
        locations conditioned on all evidence and time passage. This method
        essentially converts a list of particles into a belief distribution.

        This function should return a normalized distribution.
        """
        dist = util.Counter()

        for particle in self.particles:
            dist[particle] += 1

        dist.normalize()
        return dist

